public enum Status {
    ABERTO("Aberto"), APROVADO("Aprovado"), REPROVADO("Reprovado");

    private final String descricao;

    Status(String descricao) {
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }

    @Override
    public String toString() {
        return descricao;
    }
}